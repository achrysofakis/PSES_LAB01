clear all;
close all;
% Χρόνος από 0 έως 0.5 δευτερόλεπτα (500ms)
t = 0:0.001:0.5;

% Δημιουργία του σήματος x(t)
x_t = 5 * cos(24 * pi * t) - 2 * sin(1.5 * pi * t);

% Διάφορες περίοδοι δειγματοληψίας
Ts_values = [1/48, 1/24, 1/12];

% Σχεδιασμός των διαφόρων σηματων στα υποδιαγράμματα
for i = 1:length(Ts_values)
    Ts = Ts_values(i);
    n = 0:Ts:0.5;
    x_sampled = 5 * cos(24 * pi * n) - 2 * sin(1.5 * pi * n);
    
    % Δημιουργία νέου υποδιαγράμματος
    subplot(3, 1, i);
    
    % Σχεδίαση του αρχικού σήματος
    plot(t, x_t, 'b');
    hold on;
    
    % Σχεδίαση του δειγματοληφθέντος σήματος
    stem(n, x_sampled, 'r.');
    
    title(['Περίοδος Δειγματοληψίας Ts = ' num2str(Ts) 's']);
    xlabel('Χρόνος (s)');
    ylabel('Τιμή σήματος');
    
    % Προσθέστε τη λεγόμενη για να εμφανίσετε τα δύο γραφήματα στο ίδιο υποδιάγραμμα
    legend('Αρχικό Σήμα', 'Δειγματοληφθέν Σήμα');
end



